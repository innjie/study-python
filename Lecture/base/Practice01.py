# 정수 / 실수 자료형
print(5)  # 정수
print(-10)
print(3.14)  # 실수
print(1000)
print(5 + 3)
print(2 * 8)
print(3 * (3 + 1))

# 문자열 자료형
print('풍선')
print("나비")
print("a" * 9)

# boolean 자료형
print(5 > 10)
print(5 < 10)
print(True)
print(False)
print(not True)
print(not False)
print(not (5 > 10))  # True

# operation
print(2 ** 3)  # 8
print(5 % 2)  # 3
print(5 // 3)  # 1 (몫)
print(10 < 3)
print(5 <= 5)
print((3 > 0) and (3 < 5))  # True
